// Unary operations
public class unary{

    public static void main(String args[]){
    
        int x = 10, y = 20;
        
        System.out.println("Unary x=10 y=20");
        System.out.println("");
        System.out.println("x++ = "+x);
        System.out.println("++x = "+(++x));
        System.out.println("x-- = "+x--);
        System.out.println("--x = "+--x);
        System.out.println("++x + y++ = "+(++x + y++));
        System.out.println("");
        System.out.println(10*10/5+3-1*4/2);
    }
}